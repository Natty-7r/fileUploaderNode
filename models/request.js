const mongoose = require("mongoose");
const Schema = mongoose.Schema;
const requestsSchema = new Schema({
  from: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  amount: {
    type: Number,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
});

module.exports = mongoose.model("request", requestsSchema);
